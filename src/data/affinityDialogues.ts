/**
 * 서번트 인연 대사 시스템
 *
 * 특별한 관계가 있는 서번트 쌍이 조우/전투 시 전용 대사를 출력한다.
 * - 서번트 ID 쌍으로 매칭 (순서 무관)
 * - encounter: 조우 시 각 서번트의 대사
 * - clash: 전투 중 각 서번트의 대사
 * - specialLog: 특수 상황 묘사
 *
 * i18n 확장 시: 이 파일을 언어별 JSON으로 분리하고
 * `getAffinityDialogue(idA, idB, lang)` 형태로 전환 가능
 */

// ─── 타입 ───

export interface AffinityPair {
  /** 서번트 ID 쌍 */
  ids: [number, number];
  /** 조우 대사 — servantId → 대사 배열 (랜덤 선택) */
  encounter: Record<number, string[]>;
  /** 전투 중 대사 (선택) */
  clash?: Record<number, string[]>;
  /** 특수 상황 묘사 */
  specialLog?: string;
}

// ─── 유틸 ───

function pairKey(a: number, b: number): string {
  return a < b ? `${a}_${b}` : `${b}_${a}`;
}

